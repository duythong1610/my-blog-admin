#!/usr/bin/env sh

# abort on errors
set -e

echo '====================================================================================='
echo '=============================...DEPLOYING PRODUCTION...============================='
echo '====================================================================================='

npm run build:prod

echo '====================================================================================='
echo '=====================================...BUILD...====================================='
echo '====================================================================================='

cp .htaccess dist
cp UPDATE.md dist
cd dist

zip -r dist.zip ./ -x '*.git*'
mv dist.zip ../
cd -

green=`tput setaf 2`
reset=`tput sgr0`
now=$(date +"%T")

echo "${green}====================================================================================="
echo "${green}=======================...BUILD SUCCESS PRODUCTION AT $now...======================"
echo "${green}=====================================================================================${reset}"
